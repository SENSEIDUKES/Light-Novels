// Robust helper to parse LLM JSON responses safely
export function cleanAndParseJSON(rawText: string) {
  // 1. Remove <think> blocks (often generated by deepseek or reasoning models)
  const cleaned = rawText.replace(/<think>[\s\S]*?<\/think>/gi, '').trim();
  
  // 2. Basic markdown strip
  let stripped = cleaned;
  if (stripped.startsWith("```json")) {
    stripped = stripped.substring(7);
  } else if (stripped.startsWith("```")) {
    stripped = stripped.substring(3);
  }
  
  if (stripped.endsWith("```")) {
    stripped = stripped.substring(0, stripped.length - 3);
  }
  
  stripped = stripped.trim();
  
  try {
    return JSON.parse(stripped);
  } catch (err: any) {
    console.warn("Direct JSON parse failed, trying regex extraction");
    // 3. Fallback regex: extract content between first { and last }, or [ and ]
    const objectMatch = cleaned.match(/\{[\s\S]*\}/);
    const arrayMatch = cleaned.match(/\[[\s\S]*\]/);
    
    let matchToUse = objectMatch;
    if (arrayMatch && objectMatch) {
      if (arrayMatch.index! < objectMatch.index!) matchToUse = arrayMatch;
    } else if (arrayMatch) {
      matchToUse = arrayMatch;
    }

    if (matchToUse) {
      try {
        return JSON.parse(matchToUse[0]);
      } catch (nestedErr) {
         // Attempt to strip inner markdown if the model hallucinated it inside
         const innerClean = matchToUse[0].replace(/```json/g, '').replace(/```/g, '').trim();
         try {
           return JSON.parse(innerClean);
         } catch (e2) {
           throw new Error("Failed to parse JSON response: " + cleaned);
         }
      }
    }
    throw new Error("Failed to parse JSON response: " + cleaned);
  }
}

// Master function to route and execute text generation (Streaming mode)
export async function* routeTextGenerationStream(
  route: "storyMaker",
  systemInstruction: string,
  userPrompt: string,
  routeKey: string, // e.g., "generate-chapter"
  routingConfig?: RouteConfig,
  customKeys?: { geminiApiKey?: string; openrouterApiKey?: string; ollamaHost?: string; }
): AsyncGenerator<string, void, unknown> {
  let activeConfig: RouteConfig = (routingConfig as any)?.storyMaker || routingConfig || {
    provider: "gemini",
    model: "gemini-2.5-flash-lite"
  };
  if (!activeConfig.provider) {
    activeConfig = {
      provider: "gemini",
      model: "gemini-2.5-flash-lite"
    };
  }

  const { provider, model, temperature, maxOutputTokens } = activeConfig;
  if (process.env.NODE_ENV !== "production") {
    console.log(`[aiRouter] Streaming task '${routeKey}' via Route '${route}' -> Provider: '${provider}', Model: '${model}'`);
  }

  if (provider === "gemini") {
    const ai = getAIClient(customKeys?.geminiApiKey);
    const geminiModel = (model || "google/gemini-2.5-flash-lite").replace(/^google\//, "");
    try {
      const responseStream = await ai.models.generateContentStream({
        model: geminiModel,
        contents: userPrompt,
        config: {
          systemInstruction,
          temperature: temperature ?? DEFAULT_TEMPERATURE,
          responseMimeType: "text/plain",
          maxOutputTokens: maxOutputTokens ?? DEFAULT_MAX_TOKENS,
        }
      });

      for await (const chunk of responseStream) {
        if (chunk.text) {
          yield chunk.text;
        }
      }
    } catch (error: any) {
      console.error("[aiRouter] Gemini provider encountered error during stream:", error);
      if (error.status === 429 || (error.message && error.message.includes("429"))) {
        const apiKey = customKeys?.openrouterApiKey || process.env.OPENROUTER_API_KEY;
        if (apiKey && apiKey !== "MY_OPENROUTER_API_KEY") {
          console.warn("[aiRouter] Rate limit hit. Applying graceful fallback: Switching from Gemini to OpenRouter.");
          const fallbackModel = route === "storyMaker" ? ROUTER_PRESETS.storyMaker.openrouter[0] : "deepseek/deepseek-chat";
          
          yield* routeTextGenerationStream(
            route, 
            systemInstruction, 
            userPrompt, 
            routeKey + "_fallback", 
            { provider: "openrouter", model: fallbackModel }, 
            customKeys
          );
          return;
        }

        throw new Error(`Rate limit exceeded [429]. The model '${model}' is currently busy or rate-limited. Please try again soon, or switch to a different model in the routing configuration.`);
      }
      throw error;
    }
  } else if (provider === "openrouter") {
    const apiKey = customKeys?.openrouterApiKey || process.env.OPENROUTER_API_KEY;
    if (!apiKey || apiKey === "MY_OPENROUTER_API_KEY") {
      throw new Error("OpenRouter API key is missing.");
    }

    try {
      const payload = {
        model: model || "deepseek/deepseek-chat",
        messages: [
          { role: "system", content: systemInstruction },
          { role: "user", content: userPrompt }
        ],
        stream: true,
        temperature: temperature ?? DEFAULT_TEMPERATURE,
        max_tokens: maxOutputTokens ?? DEFAULT_MAX_TOKENS
      };

      const response = await fetch("https://openrouter.ai/api/v1/chat/completions", {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
          "Authorization": `Bearer ${apiKey}`,
          "HTTP-Referer": "https://ai.studio/build",
          "X-Title": "SEIHouse Celestial Scroll"
        },
        body: JSON.stringify(payload)
      });

      if (!response.ok) {
        const errText = await response.text();
        if (response.status === 429) {
          throw new Error(`Rate limit exceeded [429]. The model '${model}' is currently busy or rate-limited. Please try again soon, or switch to a different model in the routing configuration.`);
        }
        throw new Error(`OpenRouter gateway error [${response.status}]: ${errText}`);
      }

      if (!response.body) throw new Error("No response body.");

      const reader = response.body.getReader();
      const decoder = new TextDecoder("utf-8");
      
      let buffer = "";
      while (true) {
        const { value, done } = await reader.read();
        if (done) break;
        buffer += decoder.decode(value, { stream: true });
        
        let breakIndex = buffer.indexOf("\n");
        while (breakIndex !== -1) {
          const line = buffer.substring(0, breakIndex).trim();
          buffer = buffer.substring(breakIndex + 1);
          breakIndex = buffer.indexOf("\n");

          if (line.startsWith("data: ") && line !== "data: [DONE]") {
            try {
              const data = JSON.parse(line.substring(6));
              const content = data.choices?.[0]?.delta?.content;
              if (content) yield content;
            } catch (e) {
              // ignore parse errors for partial chunks
            }
          }
        }
      }
    } catch (error: any) {
      console.error("[aiRouter] OpenRouter provider encountered error during stream:", error);
      throw error;
    }
  } else if (provider === "ollama") {
    const host = customKeys?.ollamaHost || process.env.OLLAMA_HOST || "http://localhost:11434";
    try {
      const response = await fetch(`${host}/api/generate`, {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({
          model: model || "llama3",
          system: systemInstruction,
          prompt: userPrompt,
          stream: true,
          options: { 
            num_predict: maxOutputTokens ?? DEFAULT_MAX_TOKENS,
            temperature: temperature ?? DEFAULT_TEMPERATURE
          }
        })
      });

      if (!response.ok) {
        throw new Error(`Local Ollama gateway responded with status: ${response.status}`);
      }

      if (!response.body) throw new Error("No response body");

      const reader = response.body.getReader();
      const decoder = new TextDecoder();
      let buffer = "";
      while (true) {
        const { value, done } = await reader.read();
        if (done) break;
        buffer += decoder.decode(value, { stream: true });
        let breakIndex = buffer.indexOf("\n");
        while (breakIndex !== -1) {
          const line = buffer.substring(0, breakIndex).trim();
          buffer = buffer.substring(breakIndex + 1);
          breakIndex = buffer.indexOf("\n");

          if (line) {
            try {
              const data = JSON.parse(line);
              if (data.response) yield data.response;
            } catch (e) {
              // ignore
            }
          }
        }
      }
    } catch (error: any) {
      throw new Error(`Ollama server stream failed: ${error.message}`);
    }
  } else {
    throw new Error(`Unsupported routing provider: '${provider}'`);
  }
}

// Master function to route and execute text generation
export async function routeTextGeneration(
  route: "storyMaker",
  systemInstruction: string,
  userPrompt: string,
  routeKey: string, // e.g., "generate-chapter", "steer-arc" etc.
  routingConfig?: RouteConfig,
  customKeys?: { geminiApiKey?: string; openrouterApiKey?: string; ollamaHost?: string; },
  responseSchema?: any
): Promise<any> {
  let activeConfig: RouteConfig = (routingConfig as any)?.storyMaker || routingConfig || {
    provider: "gemini",
    model: "gemini-2.5-flash-lite"
  };
  if (!activeConfig.provider) {
    activeConfig = {
      provider: "gemini",
      model: "gemini-2.5-flash-lite"
    };
  }

  const { provider, model, temperature, maxOutputTokens } = activeConfig;
  if (process.env.NODE_ENV !== "production") {
    console.log(`[aiRouter] Routing task '${routeKey}' via Route '${route}' -> Provider: '${provider}', Model: '${model}'`);
  }

  if (provider === "gemini") {
    // -------------------------------------------------------------
    // GOOGLE GEMINI ROUTE
    // -------------------------------------------------------------
    const ai = getAIClient(customKeys?.geminiApiKey);
    const geminiModel = (model || "google/gemini-2.5-flash-lite").replace(/^google\//, "");
    try {
      const config: any = {
        systemInstruction,
        responseMimeType: "application/json",
        temperature: temperature ?? DEFAULT_TEMPERATURE,
        maxOutputTokens: maxOutputTokens ?? DEFAULT_MAX_TOKENS,
      };
      
      if (responseSchema) {
        config.responseSchema = responseSchema;
      }

      const response = await ai.models.generateContent({
        model: geminiModel,
        contents: userPrompt,
        config
      });

      if (!response.text) {
        throw new Error("No text response received from Gemini.");
      }

      return cleanAndParseJSON(response.text);
    } catch (error: any) {
      console.error("[aiRouter] Gemini provider encountered error:", error);
      if (error.status === 429 || (error.message && error.message.includes("429"))) {
        const apiKey = customKeys?.openrouterApiKey || process.env.OPENROUTER_API_KEY;
        if (apiKey && apiKey !== "MY_OPENROUTER_API_KEY") {
          console.warn("[aiRouter] Rate limit hit. Applying graceful fallback: Switching from Gemini to OpenRouter.");
          const fallbackModel = route === "storyMaker" ? ROUTER_PRESETS.storyMaker.openrouter[0] : "deepseek/deepseek-chat";
          
          return routeTextGeneration(
            route, 
            systemInstruction, 
            userPrompt, 
            routeKey + "_fallback", 
            { provider: "openrouter", model: fallbackModel }, 
            customKeys
          );
        }

        throw new Error(`Rate limit exceeded [429]. The model '${model}' is currently busy or rate-limited. Please try again soon, or switch to a different model in the routing configuration.`);
      }
      throw error;
    }
  } else if (provider === "openrouter") {
    // -------------------------------------------------------------
    // OPENROUTER ROUTE
    // -------------------------------------------------------------
    const apiKey = customKeys?.openrouterApiKey || process.env.OPENROUTER_API_KEY;
    if (!apiKey || apiKey === "MY_OPENROUTER_API_KEY") {
      throw new Error("OpenRouter API key is missing. Please supply a valid key in the Router settings or declare OPENROUTER_API_KEY in Settings > Secrets panel.");
    }

    try {
      const payload = {
        model: model || "deepseek/deepseek-chat",
        messages: [
          { role: "system", content: systemInstruction },
          { role: "user", content: userPrompt }
        ],
        response_format: { type: "json_object" },
        temperature: temperature ?? DEFAULT_TEMPERATURE,
        max_tokens: maxOutputTokens ?? DEFAULT_MAX_TOKENS
      };

      const response = await fetch("https://openrouter.ai/api/v1/chat/completions", {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
          "Authorization": `Bearer ${apiKey}`,
          "HTTP-Referer": "https://ai.studio/build",
          "X-Title": "SEIHouse Celestial Scroll"
        },
        body: JSON.stringify(payload)
      });

      if (!response.ok) {
        const errText = await response.text();
        if (response.status === 429) {
          throw new Error(`Rate limit exceeded [429]. The model '${model}' is currently busy or rate-limited. Please try again soon, or switch to a different model in the routing configuration.`);
        }
        throw new Error(`OpenRouter gateway error [${response.status}]: ${errText}`);
      }

      const resJson = await response.json();
      const content = resJson.choices?.[0]?.message?.content;
      if (!content) {
        throw new Error("Empty message content returned from OpenRouter.");
      }

      return cleanAndParseJSON(content);
    } catch (error: any) {
      console.error("[aiRouter] OpenRouter provider encountered error:", error);
      throw error;
    }
  } else if (provider === "ollama") {
    // -------------------------------------------------------------
    // OLLAMA (LOCAL) ROUTE
    // -------------------------------------------------------------
    const host = customKeys?.ollamaHost || process.env.OLLAMA_HOST || "http://localhost:11434";
    try {
      const response = await fetch(`${host}/api/generate`, {
        method: "POST",
        headers: {
          "Content-Type": "application/json"
        },
        body: JSON.stringify({
          model: model || "llama3",
          system: systemInstruction,
          prompt: userPrompt,
          stream: false,
          format: "json",
          options: { 
            num_predict: maxOutputTokens ?? DEFAULT_MAX_TOKENS,
            temperature: temperature ?? DEFAULT_TEMPERATURE
          }
        })
      });

      if (!response.ok) {
        throw new Error(`Local Ollama gateway responded with status: ${response.status}`);
      }

      const resJson = await response.json();
      const content = resJson.response;
      if (!content) {
        throw new Error("Empty response returned from local Ollama model.");
      }

      return cleanAndParseJSON(content);
    } catch (error: any) {
      console.error("[aiRouter] Ollama provider encountered error:", error);
      throw new Error(`Ollama server at ${host} is unreachable. Please verify Ollama is running locally and CORS is enabled via OLLAMA_ORIGINS="*" before calling Local route: ${error.message}`);
    }
  } else {
    throw new Error(`Unsupported routing provider: '${provider}'`);
  }
}

// Master function to route and execute image generation
export async function routeImageGeneration(
  prompt: string,
  type?: string,
  routingConfig?: RouteConfig,
  customKeys?: { geminiApiKey?: string; openrouterApiKey?: string; ollamaHost?: string; }
): Promise<{ imageUrls: string[]; note?: string; isFallback?: boolean }> {
  let activeConfig: RouteConfig = (routingConfig as any)?.imageGenerator || routingConfig || {
    provider: "gemini",
    model: "google/gemini-3.1-flash-image"
  };
  if (!activeConfig.provider) {
    activeConfig = {
      provider: "gemini",
      model: "google/gemini-3.1-flash-image"
    };
  }

  const { provider, model } = activeConfig;
  if (process.env.NODE_ENV !== "production") {
    console.log(`[aiRouter] Routing Image task -> Provider: '${provider}', Model: '${model}'`);
  }

  const styleEnhancer = type === "location"
    ? "mystical landscape, fantasy environment concept art, high-energy light novel scenery, dramatic lighting, celestial aura, beautiful composition, vibrant colors"
    : type === "chapterHero"
    ? "Visual Memory: stark, stylized cinematic illustration, dramatic crucial moment frozen in time. SEIHouse canonical brand palette: #000000, #FAFAFA, #8B0000, #04ACFF. Emotional core, meaning-first creator artwork, high contrast, immersive mood piece."
    : "professional anime character portrait, fantasy webnovel cover design, intricate details, sharp focus, celestial backlighting, clean high contrast colors";
  
  const rawPrompt = `${prompt}. Style: ${styleEnhancer}. Solo subject, centered, no borders, no text.`;

  // Standard high-quality local fallbacks (Dynamic Pollinations AI matching the prompt, robust and keyless)
  const getFallbackImages = (count: number) => {
    const seeds = [];
    for (let i = 0; i < count; i++) {
        const seedValue = prompt 
          ? Math.abs(String(prompt).split("").reduce((acc, char) => acc + char.charCodeAt(0), 0) + i * 137)
          : Math.floor(Math.random() * 10000) + i;
        seeds.push(`https://image.pollinations.ai/prompt/${encodeURIComponent(rawPrompt)}?width=512&height=512&nologo=true&seed=${seedValue}`);
    }
    return seeds;
  };

  if (provider === "gemini") {
    // -------------------------------------------------------------
    // GEMINI IMAGE GEN
    // -------------------------------------------------------------
    const ai = getAIClient(customKeys?.geminiApiKey);
    try {
      const gModel = (model || "google/gemini-3.1-flash-image").replace(/^google\//, "");
      let imageUrls: string[] = [];

      if (gModel.startsWith("imagen-")) {
        // Imagen generation
        const response = await ai.models.generateImages({
          model: gModel,
          prompt: rawPrompt,
          config: {
            numberOfImages: 3,
            outputMimeType: 'image/jpeg',
            aspectRatio: '1:1',
          },
        });
        if (response.generatedImages && response.generatedImages.length > 0) {
            imageUrls = response.generatedImages.map((img: any) => `data:image/jpeg;base64,${img.image.imageBytes}`);
        }
      } else {
        // Nano banana (Gemini Flash Image) generation
        // Requires sequential or parallel calls since it only generated 1 at a time?
        const generateOne = async (idx: number) => {
          if (idx > 0) {
            await new Promise(resolve => setTimeout(resolve, idx * 300));
          }
          try {
            const response = await ai.models.generateContent({
              model: gModel,
              contents: {
                parts: [
                  {
                    text: rawPrompt,
                  },
                ],
              },
              config: {
                imageConfig: {
                  aspectRatio: "1:1"
                }
              }
            });

            if (response.candidates?.[0]?.content?.parts) {
              for (const part of response.candidates[0].content.parts) {
                if (part.inlineData?.data) {
                  return `data:image/png;base64,${part.inlineData.data}`;
                }
              }
            }
          } catch (e: any) {
            console.warn(`[aiRouter] Single image generation attempt ${idx + 1} failed:`, e);
          }
          return null;
        };

        const results = await Promise.all([generateOne(0), generateOne(1), generateOne(2)]);
        imageUrls = results.filter((url): url is string => url !== null);
      }

      if (imageUrls.length === 0) {
        throw new Error("No image frames returned in Gemini payload.");
      }

      // If less than 3, just pad with the first one so we have 3
      while (imageUrls.length < 3) {
          imageUrls.push(imageUrls[0]);
      }

      return { imageUrls };
    } catch (error: any) {
      console.warn("[aiRouter] Gemini image gen failed, serving fallback:", error);
      return {
        imageUrls: getFallbackImages(3),
        note: `Projected via cosmic fallback: ${error.message || "quota reserve limit reached"}.`,
        isFallback: true
      };
    }
  } else if (provider === "openrouter") {
    // -------------------------------------------------------------
    // OPENROUTER IMAGE GEN (PROMPT TUNNEL via Pollinations AI)
    // -------------------------------------------------------------
    // OpenRouter is designed primarily for text-based models (and multimodal inputs).
    // It does not natively support text-to-image API generation endpoints (returning 404).
    // To supply a world-class, premium visual experience, we redirect OpenRouter image generator 
    // selections to our dynamic keyless neural engine (Pollinations AI + FLUX) so that 
    // your custom prompt actually synthesizes three magnificent unique visual renders!
    const imageUrls = getFallbackImages(3);
    return {
      imageUrls,
      note: `Synthesized via neural core using direct client-side visualizer (based on prompt: "${rawPrompt}").`
    };
  } else if (provider === "ollama") {
    // -------------------------------------------------------------
    // OLLAMA LOCAL IMAGE GEN (LOCAL TEXT EXPLANATION)
    // -------------------------------------------------------------
    // Local Ollama is normally text-based. Let's make it a stellar simulation:
    // It will "forge" or "synthesize" a gorgeous Local Prompt, and use Unsplash
    // as a visual medium while providing instructions on local SD integrations.
    const h = customKeys?.ollamaHost || process.env.OLLAMA_HOST || "http://localhost:11434";
    return {
      imageUrls: getFallbackImages(3),
      note: `Local Ollama (${model}) generated prompt: "${rawPrompt}". Standard Ollama is text-only; we synthesized a beautiful representation of your query.`,
      isFallback: true
    };
  } else {
    throw new Error(`Unsupported routing provider: '${provider}'`);
  }
}
